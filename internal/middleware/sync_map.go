package middleware

import "sync"

// typedSyncMap — тонкая обобщённая (generic) обёртка над sync.Map.
// Назначение — убрать приведения типа (`v.(V)`) из вызывающего кода:
// внутри обёртки они остаются (сама sync.Map нетипизирована, дженерики
// в stdlib до неё пока не доехали), но наружу API уже проверяется на
// этапе компиляции.
//
// Это НЕ расширение функционала, а прозрачная обёртка. Все
// характеристики производительности sync.Map сохранены: паттерн
// «редкая запись — частое чтение», горячий путь без блокировок на
// чтении существующего ключа и т.д. — обёртка не добавляет никаких
// локов и аллокаций сверху.
//
// Работает с нулевым значением, make не нужен. Указательный приёмник
// (pointer receiver) на методах обязателен: sync.Map нельзя
// копировать — она держит внутренний мьютекс и счётчики, копия
// сломает инвариант.
//
// Если хочется готового и более фичастого решения — сторонняя
// библиотека github.com/puzpuzpuz/xsync даёт MapOf[K, V] с тем же
// API и парой дополнительных оптимизаций.
type typedSyncMap[K comparable, V any] struct {
	m sync.Map
}

// Load возвращает значение по ключу. Если ключа нет, второй возврат
// false, а первый — нулевое значение типа V (nil для указателей, 0
// для int и т.д.).
func (s *typedSyncMap[K, V]) Load(key K) (V, bool) {
	v, ok := s.m.Load(key)
	if !ok {
		var zero V
		return zero, false
	}
	// Приведение типа безопасно по построению: пишут в s.m только
	// наши же методы Store/LoadOrStore ниже, оба принимают V. Чужого
	// типа здесь оказаться не может.
	return v.(V), true
}

// LoadOrStore возвращает существующее значение, либо записывает и
// возвращает переданное value. loaded=true, если значение уже было;
// false, если записали своё. Операция атомарна — безопасна при гонке
// «две горутины одновременно создают запись на один ключ».
func (s *typedSyncMap[K, V]) LoadOrStore(key K, value V) (actual V, loaded bool) {
	a, loaded := s.m.LoadOrStore(key, value)
	return a.(V), loaded
}
